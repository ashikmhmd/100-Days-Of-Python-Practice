# Here is how to update variable which u declared!
# Suppose you have a variable called X = 10 which adds 1 every step.
X = 10
# Now you can update manually like this
X = X + 1
print(X)
# Or you can write like this
X += 1
print(X)
# You can do this for subtraction, division...like
X -= 1
print(X)
# When you changes the variable and print the same variable again it will print the updated info!
X /=2
print(X)
# At this point X = 5.5 ; this is how it updates!

